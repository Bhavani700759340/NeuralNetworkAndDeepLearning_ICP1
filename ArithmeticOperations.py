'''Take two numbers from user and perform at least 4 arithmetic operations on them.'''
num1 = int(input("Enter number 1 : "))
num2 = int(input("Enter number 2 : "))

#operation addition
print("Result of addition operation ",num1+num2)

#operation subtraction
print("Result of subtraction operation ",num1-num2)

#operation multiplication
print("Result of multiplication operation ",num1*num2)

#operation modulo
print("Result of modulus operation ",num1%num2)